# You can return a range of characters by using the slice syntax.
b = "Hello, World!"
print(b[2:5])


# Slice From the Start:
b = "Hello, World!"
print(b[:5])


# Slice To the End:
b = "Hello, World!"
print(b[2:])


# Use negative indexes to start the slice from the end of the string:
b = "Hello, World!"
print(b[-5:-2])